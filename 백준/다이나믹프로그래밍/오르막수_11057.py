"""
오르막 수

시간 제한	메모리 제한	제출	정답	맞힌 사람	정답 비율
1 초	256 MB	35837	17402	13379	47.269%

문제
오르막 수는 수의 자리가 오름차순을 이루는 수를 말한다.
이때, 인접한 수가 같아도 오름차순으로 친다.

예를 들어, 2234와 3678, 11119는 오르막 수이지만,
2232, 3676, 91111은 오르막 수가 아니다.

수의 길이 N이 주어졌을 때, 오르막 수의 개수를 구하는 프로그램을 작성하시오.
수는 0으로 시작할 수 있다.

입력
첫째 줄에 N (1 ≤ N ≤ 1,000)이 주어진다.

출력
첫째 줄에 길이가 N인 오르막 수의 개수를 10,007로 나눈 나머지를 출력한다.

"""

if __name__ == "__main__":
    import sys

    n = int(sys.stdin.readline().rstrip())

    d = [[i for i in range(0,10)] for j in range(n+1)]

    for i in range(0,10):
        d[1][i] = 1

    for i in range(2, n+1):
        for j in range(0,10):
            ans = 0
            for k in range(0,j+1):
                ans += d[i-1][k]
            d[i][j] = ans%10007

    # print(d)

    print(sum(d[n])%10007)




